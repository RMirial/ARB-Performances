    <h2 style="text-decoration: underline;">Adhésions & dons</h2>

    <!--présentation-->

    <section id="texte_p">
        <p>Informations sur l’adhésion et le don à l’association ARB-Performances<br>
            La cotisation annuelle est fixée à 30€, il s’agit d’une cotisation fixe. Vous pouvez aussi réaliser un don
            indépendant de cette
            cotisation qui sera intégralement reversée au Team ARB-Performances.<br>
            <br>
            ARB-Performances est une association, vos remarques, suggestions et soutiens divers sont les bienvenus.
            Le bureau tient à votre disposition l’ensemble des documents relatifs au fonctionnement de l’association
            (statuts, AG, budgets, calendrier…)<br>
            <br>
            Pourquoi adhérer à l’association ARB-Performances?<br>
            <br>
            Nous avons choisi le statut associatif pour la simplicité de création, la possibilité de partenariats mais
            aussi pour
            ouvrir ARB-Performances aux adhérents et aux dons…<br>
            <br>
            Nous proposons aux adhérents qui le souhaitent de se joindre au Team lors des entraînements et en course
            mais aussi de bénéficier
            des remises et avantages négociés au nom de l’association auprès des partenaires (Honda, Milton Services,
            RacingLubes, BLB,
            Crédit Agricole d’Aquitaine, Glasurit…)<br>
            <br>
            Ainsi, l’adhésion permet à tout les adhérents de ARB-Performances de participer activement à l’association
            et d’intervenir comme
            acteurs privilégiés le temps d’un weekend de course. Les disponibilités de chacun seront prises en compte en
            fonction de chaque événement.<br>
            <br>
            Nous vous conseillerons, accompagnerons et pourrons vous faire bénéficier d’une partie de notre logistique.
            Avant, pendant et
            après chaque événement, nous pourrons vous donner les conseils nécessaires à l’apprentissage de la moto sur
            piste.<br>
            <br>
            La page Facebook du Team permettra aussi aux adhérents et aux pilotes d’échanger, planifier et commenter les
            événements à venir mais
            aussi à faire partager les retours d’expériences de chacun.<br>
            <br>
            Bien sûr, les bénévoles non adhérents sont aussi les bienvenus, mais ne sont pas prioritaires lorsque les
            places sont limitées.<br>
            <br>
            ———————————————————————————–
            <br>
            Comment adhérer à l’association?<br>
            <br>
            Vous pouvez adhérer à l’association ARB-Performances à tout moment, la cotisation est valable pour l’année
            en cours.
            Le Team ARB-Performances a opté pour l’interface en ligne HelloAsso pour le paiement des cotisations. Aucun
            frais n’est à prévoir,
            la totalité de votre cotisation reviendra à l’association.<br>
            Attention, le système HelloAsso vous propose automatiquement d’attribuer « un pourboire », il est
            facultatif, modifiable ou annulable.<br>
            <br>
            Il est de même possible pour ceux qui le souhaitent, de faire parvenir votre règlement par chèque
            directement au bureau administratif de
            ARB-Performances. Le chèque doit être à l’ordre de « ARB-Performances » et adressé par courrier à
            ARB-Performances, 28 route de Bernin,
            33650 Martillac. N’oubliez pas d’indiquer votre mail au dos du chèque.<br>
            <br>
            Après votre adhésion vous recevrez un mail de confirmation et une carte de membre dans les jours qui
            suivent.<br>
            Une fois adhérent, n’hésitez pas également à nous communiquer votre photo, elle sera ajoutée au
            trombinoscope de l’association.<br>
            <br>
            Vous souhaitez rejoindre ARB-Performances en tant que adhérent? Alors cliquez sur la bannière ci-dessous et
            vous serez redirigez vers
            l’interface HelloAsso.<br>
            <br>
            ———————————————————————————-
            <br>
            Peut-on faire un don à l’association sans forcément y adhérer?<br>
            <br>
            Oui, aucun frais n’est à prévoir, la totalité de votre don reviendra à l’association.<br>
            <br>
            Attention, le système HelloAsso vous propose automatiquement d’attribuer « un pourboire », il est
            facultatif, modifiable ou annulable.<br>
            <br>
            Vous avez la possibilité de faire un don d’un montant pré établit mais aussi du montant que vous souhaitez,
            ponctuel ou mensuel.<br>
            <br>
            Ce lien vous permet aussi de régulariser votre situation auprès de ARB-Performances suite à des événements
            divers (repas, achats de pièces, remboursement d’une commande…).
            <br>
            Si vous êtes donateur privé et que vous souhaitez apparaître sur la page de nos partenaires, n’hésitez pas à
            le signaler.<br>
            Pour les entreprises donatrices, votre logo et vos coordonnées sont automatiquement affichés sur notre page
            partenaire et sur notre
            communication paddock.<br>
            <br>
            Il est de même possible pour ceux qui le souhaitent, de faire parvenir votre don par chèque directement au
            bureau administratif de
            ARB-Performances. Le chèque doit être à l’ordre de « ARB-Performances » et adressé par courrier à
            ARB-Performances, 28 route de Bernin,
            33650 Martillac. N’oubliez pas d’indiquer votre mail au dos du chèque.<br>
            <br>
            ———————————————————————————-
            <br>
            En plus de l’accompagnement et du conseil pour les pilotes amateurs, ARB-Performances propose à ses
            adhérents de faire partie
            d’une aventure, de suivre un Team amateur et de vivre le monde de la course moto de l’intérieur!<br>
            <br>
            Rejoignez nous au sein de ARB-Performances et vivez l’expérience à nos côtés!<br>
            <br>
            L’équipe ARB-Performances vous remercie.<br>
    </section>

